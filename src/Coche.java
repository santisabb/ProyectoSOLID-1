public class Coche {
    int cantidadPuertas;
    double precio;
    String marca;

    public Coche() {
        this.marca = marca;
        this.precio = precio;
        this.cantidadPuertas = cantidadPuertas;
    }


    public int getCantidadPuertas() {
        return cantidadPuertas;
    }


    public double getPrecio(){
        return precio;
    }


    public String getMarca() {
        return marca;
    }


}
