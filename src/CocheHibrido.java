public class CocheHibrido extends Coche{
    double duracionBateria;
    boolean modoElectrico;

    public double getDuracionBateria() {
        return duracionBateria;
    }

    public void setDuracionBateria(double duracionBateria) {
        this.duracionBateria = duracionBateria;
    }

    public boolean isModoElectrico() {
        return modoElectrico;
    }

    public void setModoElectrico(boolean modoElectrico) {
        this.modoElectrico = modoElectrico;
    }
}
